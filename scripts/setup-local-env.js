#!/usr/bin/env node

/**
 * Setup Local Environment for Development
 * Helps configure Supabase credentials in localStorage
 */

console.log('🔧 FlagFit Pro - Local Environment Setup\n');

console.log('For local development, you need to set Supabase credentials.\n');
console.log('Option 1: Create a .env.local file with:');
console.log('  VITE_SUPABASE_URL=https://pvziciccwxgftcielknm.supabase.co');
console.log('  VITE_SUPABASE_ANON_KEY=your_anon_key_here\n');

console.log('Option 2: Open your browser console and run:');
console.log('  localStorage.setItem("SUPABASE_URL", "https://pvziciccwxgftcielknm.supabase.co")');
console.log('  localStorage.setItem("SUPABASE_ANON_KEY", "your_anon_key_here")\n');

console.log('Option 3: Create .env.local file automatically:');
console.log('  npm run setup:env\n');

console.log('⚠️  NEVER commit .env.local to git!');
console.log('✅ .env.local is already in .gitignore\n');

// Check if .env file exists and read credentials
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const envPath = path.join(__dirname, '../.env');
const envLocalPath = path.join(__dirname, '../.env.local');

if (fs.existsSync(envPath)) {
  console.log('📄 Found .env file. Reading Supabase credentials...\n');

  const envContent = fs.readFileSync(envPath, 'utf8');
  const supabaseUrl = envContent.match(/SUPABASE_URL=(.+)/)?.[1];
  const supabaseAnonKey = envContent.match(/SUPABASE_ANON_KEY=(.+)/)?.[1];

  if (supabaseUrl && supabaseAnonKey) {
    console.log('✅ Supabase URL:', supabaseUrl);
    console.log('✅ Supabase Anon Key:', supabaseAnonKey.substring(0, 20) + '...\n');

    // Create .env.local file
    const envLocalContent = `# Local Development Environment
# Generated by setup-local-env.js
# DO NOT COMMIT THIS FILE

VITE_SUPABASE_URL=${supabaseUrl}
VITE_SUPABASE_ANON_KEY=${supabaseAnonKey}
`;

    fs.writeFileSync(envLocalPath, envLocalContent);
    console.log('✅ Created .env.local file with Supabase credentials');
    console.log('✅ You can now run: npm run dev\n');
  } else {
    console.log('⚠️  Could not find SUPABASE_URL or SUPABASE_ANON_KEY in .env file');
    console.log('Please add them manually.\n');
  }
} else {
  console.log('⚠️  No .env file found. Please create one first.\n');
}

console.log('🚀 Setup complete! Run "npm run dev" to start development server.');
