// Regenerate src/app/shared/offline-mdi-icons.ts from the bundled @iconify-json/mdi.
// Run: node scripts/gen-offline-mdi-icons.mjs  (dev-only; the output is committed).
// Why: <iconify-icon> otherwise fetches mdi.json from api.iconify.design at runtime,
// which 504s on slow/blocked networks. Registering the used icons offline = no CDN.
import { readFileSync, writeFileSync } from "node:fs";
import { getIconData } from "@iconify/utils";
import { createRequire } from "node:module";
const require = createRequire(import.meta.url);
const collection = require("@iconify-json/mdi/icons.json");

// Every mdi:* name referenced under src/app (static + dynamic bindings).
const NAMES = [
  "barley",
  "bottle-tonic",
  "bread-slice",
  "cart-outline",
  "coffee",
  "cup",
  "cup-water",
  "egg",
  "fish",
  "food-apple",
  "food-drumstick",
  "leaf",
  "lightning-bolt",
  "pasta",
  "peanut",
  "pill",
  "rice",
  "shield-half-full",
];

const out = {};
const missing = [];
for (const n of NAMES) {
  const data = getIconData(collection, n);
  if (!data) {
    missing.push(n);
    continue;
  }
  out[`mdi:${n}`] = {
    body: data.body,
    width: data.width ?? 24,
    height: data.height ?? 24,
  };
}
if (missing.length) {
  console.error("MISSING from mdi:", missing);
  process.exit(1);
}

const ts = `// AUTO-GENERATED by scripts/gen-offline-mdi-icons.mjs — do not edit by hand.
// Offline MDI icon data registered in main.ts so <iconify-icon> never fetches from
// api.iconify.design (those requests 504 on slow/blocked networks). Add a name to
// scripts/gen-offline-mdi-icons.mjs and re-run to extend.
export interface OfflineIcon { body: string; width: number; height: number; }
export const OFFLINE_MDI_ICONS: Record<string, OfflineIcon> = ${JSON.stringify(out, null, 2)};
`;
writeFileSync("src/app/shared/offline-mdi-icons.ts", ts);
console.log(`wrote ${Object.keys(out).length} offline mdi icons`);
